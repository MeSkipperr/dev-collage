public class OperatorAritmatika {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        int hasil;

        hasil = a + b;
        System.out.println("Hasil dari a + b : " + hasil);

        hasil = a - b;
        System.out.println("Hasil dari a - b : " + hasil);

        hasil = a * b;
        System.out.println("Hasil dari a * b : " + hasil);

        hasil = a / b;
        System.out.println("Hasil dari a / b : " + hasil);

        hasil = a % b;
        System.out.println("Hasil dari a % b : " + hasil);

        
    }
}