public class OperatorPembanding {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        boolean hasil;

        hasil = a > b;
        System.out.println("Hasil dari a > b : " + hasil);

        hasil = a < b;
        System.out.println("Hasil dari a < b : " + hasil);

        hasil = a == b;
        System.out.println("Hasil dari a == b : " + hasil);

        hasil = a != b;
        System.out.println("Hasil dari a != b : " + hasil);

        hasil = a >= b;
        System.out.println("Hasil dari a >= b : " + hasil);

        hasil = a <= b;
        System.out.println("Hasil dari a <= b : " + hasil);
    }
}